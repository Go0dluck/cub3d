#include "../inc/cub3d.h"

void	ft_error(char *str)
{
	write(1, "Error\n", 6);
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
	//free all
	exit(0);
}
